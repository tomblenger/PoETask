DWORD	GetKeyByIDFromResource(std::wstring id)
{
	for (int i = 0; i < QUEST_RES_COUNT; i++)
	{
		if (id == g_pResQuest[i].id)
		{
			return g_pResQuest[i].dwKey;
		}
	}
	return QUEST_RES_COUNT + 1;
}