PoEResWorldArea *   GetWorldAreaResourceByID(DWORD dwAreaID)
{
	DWORD i;

	for (i = 0; i < WORLDAREA_RES_COUNT; i++)
	{
		if (g_pResWorldArea[i].dwID == dwAreaID)
		{
			return &g_pResWorldArea[i];
		}
	}

	return NULL;
}

PoEResWorldArea *   GetWorldAreaResourceByName(const WCHAR *szAreaName)
{
	DWORD i;

	for (i = 0; i < WORLDAREA_RES_COUNT; i++)
	{
		if (!wcscmp(g_pResWorldArea[i].szName, szAreaName))
		{
			return &g_pResWorldArea[i];
		}
	}

	return NULL;
}
